export function getTechniquesByCategory(category: TechniqueCategory): Technique[] {
  return TECHNIQUES_DB.filter(t => t.category === category);
}

export function searchTechniques(keyword: string): Technique[] {
  const kw = keyword.toLowerCase();
  return TECHNIQUES_DB.filter(t =>
    t.name.toLowerCase().includes(kw) ||
    t.description.toLowerCase().includes(kw) ||
    t.category.toLowerCase().includes(kw) ||
    (t.when && t.when.toLowerCase().includes(kw))
  );
}

export function getTechniquesByDifficulty(difficulty: Technique["difficulty"]): Technique[] {
  return TECHNIQUES_DB.filter(t => t.difficulty === difficulty);
}

export function getRelatedTechniques(id: string): Technique[] {
  const tech = TECHNIQUES_DB.find(t => t.id === id);
  if (!tech?.related) return [];
  return TECHNIQUES_DB.filter(t => tech.related!.some(r => t.category.includes(r) || t.id === r));
}

